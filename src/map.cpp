Map::Map()
{
    dirt = TextureManager::loadTexture("/Users/james/repos/cppgame/imgs/dirt.png");
    grass = TextureManager::loadTexture("/Users/james/repos/cppgame/imgs/grass.png");
    water = TextureManager::loadTexture("/Users/james/repos/cppgame/imgs/water.png");

    loadMap(lvl1);

    src.x = src.y = 0;
    src.w = dest.h = 32;
    src.h = dest.h = 32;

    dest.x = dest.y = 0;
}

void Map::loadMap(int arr[20][25])
{
    for (int row = 0; row < 20; row++)
    {
        for (int col = 0; col < 25; col++)
        {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::drawMap()
{
    int type = 0;

    for (int row = 0; row < 20; row++)
    {
        for (int col = 0; col < 25; col++)
        {
            type = map[row][col];

            dest.x = col * 32;
            dest.y = row * 32;

            switch (type)
            {
            case 0:
                TextureManager::draw(water, src, dest);
                break;
            case 1:
                TextureManager::draw(grass, src, dest);
                break;
            case 2:
                TextureManager::draw(dirt, src, dest);
                break;
            default:
                break;
            }
        }
    }
}